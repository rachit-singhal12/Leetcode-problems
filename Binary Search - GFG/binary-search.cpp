class Solution {
  public:
    int binarysearch(int arr[], int n, int k) {
        int count=0,index;
        for(int i=0;i<n;i++)
        {
            if(k == arr[i]){
            count=1;
            index = i;
            break;
            }
        }
        if(count==0)
            return -1;
        else
            return index;
    }
};

